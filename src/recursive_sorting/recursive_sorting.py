def merge(arrA, arrB):
    elements = len(arrA) + len(arrB)
    merged_arr = [0] * elements

    for i in arrA:
        for j in arrB:
            if arrA[i] > arrB[j]:
                merged_arr.append(arrB[j])
            else:
                merged_arr.append(arrA[i])


    return merged_arr


